#
# 아래 설명 : 우리은행은 예금분류(일반예금, 특별예금 등)의 원금, 예치기간, 이자율을
# 관리할 필요가 있다. 또한 원금에 대한 이자율을 적용하여 계산된 이자에 대해서도
# 속성으로 관리하고자 한다. 예를 들어 원금이 1000원이고 예치기간이 5개월이며
# 이자율이 5.0%라는 속성을 관리하고 계산된 이자도 관리한다. 일반예금이나 특별예금 등에 대해서는
# 코드를 부여(예. 01-일반예금, 02-특별예금 등)하여 관리한다.
#
# 1. 일반예금은 코드 엔터티를 별도로 구분하고 값에는 코드값만 포함한다.
# 2. 원금, 예치기간은 기본(BASIC)속성이다.
# 3. 이자와 이자율은 파생(DERIVED)속성이다.
# 4. 예금분류는 설계(DESIGNED)속성이다.
